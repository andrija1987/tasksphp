            $column->SetMaxLength(75);
            $column->SetFullTextWindowHandlerName('usersGrid_password_handler_list');
            $column->SetDescription($this->RenderText(''));
            $column->SetFixedWidth(null);
            $grid->AddViewColumn($column);
        }
    
